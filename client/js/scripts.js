function getData(){
	// find element of id user-search and get its value, which is the typed input from user
	var userSearch = document.getElementById('user-search').value;
	for (var i = 0; i < zodiacs.length; i++) {
		if (userSearch.toLowerCase() === zodiacs[i].name.toLowerCase()) {
			document.getElementById("name").innerHTML = zodiacs[i].name;
			document.getElementById("date").innerHTML = zodiacs[i].month;
			document.getElementById("zodiac-img").src = zodiacs[i].image;
			// innerHTML == inner content of <p>
			document.getElementById("text-result").innerHTML = zodiacs[i].description;
		}
	}
}